public class DectoBin {
    public static void main(String ar[]){
        //DectoBin
        int i=13;
        System.out.println(Integer.toBinaryString(i));
        //DectoOct
        int ii=81;
        System.out.println(Integer.toOctalString(ii));
        //DectoHex
        int iii=15;
        System.out.println(Integer.toHexString(iii));
    }
    
}
