

public class BintoDec {
    public static void main(String ar[]){
        //bin to dec
        String bin="111";
        System.out.println(Integer.parseInt(bin,3));
        //oct to Decimal
        String oct="4681";
        System.out.println(Integer.parseInt(oct,9));
        //hex to decimal
        String hex="EA34";
        System.out.println(Integer.parseInt(hex,16));
    }
    
}
